package grammar;

public class Variable {

	public static void main(String[]args) {
		// 1. 변수 
		
		int i ; //변수 선언
		i = 10; //변수 초기화
		
		System.out.println(i);
		
		int j = 20; //변수 초기화와 선언을 동시
		System.out.println(j);
		
		int num = 0;
		System.out.println(num);
		num = 10;
		System.out.println(num);
		num = 30;
		System.out.println(num);
		//변수에 저장된 데이터는 언제든지 변경할 수 있다.
	}
}
